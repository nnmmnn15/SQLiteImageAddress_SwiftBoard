    var addressList: [AddressBook] = [] // 데이터를 저장할 배열

    override func viewDidLoad() {
        super.viewDidLoad()

        fetchData()
    }

    // SQLite 데이터 가져오기
    func fetchData() {
        addressList = DatabaseManager.shared.fetchAll()
        tableView.reloadData()
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        // #warning Incomplete implementation, return the number of sections
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        return addressList.count
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        guard let cell = tableView.dequeueReusableCell(withIdentifier: "myCell", for: indexPath) as? TableViewCell else {
            fatalError()
        }

        let record = addressList[indexPath.row]

        // 데이터 설정
        cell.lblName.text = record.name
        cell.lblPhoneNumber.text = record.phoneNumber
        cell.customImageView.image = record.image ?? UIImage(systemName: "person.fill")

        return cell
    }

